# Section 5: キャッシュ仕様
# ---------------------------------------------------------------------------
st.header("キャッシュ仕様")

st.markdown(
    """
- **TTL**: `fetchers/__init__.py` の `CACHE_TTL_MINUTES = 30`（30 分）。
  期限切れ行は次回 `get()` 時に lazily 削除される。
- **保存先**: `store/news_cache.db`（環境変数 `AI_NEWS_CACHE_DB` で上書き可）。
- **動作**: `set()` は UPSERT、`get()` は欠損／期限切れで `None` を返す。
- **強制再取得**: サイドバーの **Refresh** ボタンで `force_refresh=True` を渡し、
  キャッシュを無視して全ソースを取り直す。
"""
)

